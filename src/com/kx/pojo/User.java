package com.kx.pojo;

/**
 * 用户类
 * 
 * @author 郝利寒
 *
 */

public class User {
	private int u_id;// 用户编号
	private String u_code;// 用户账户
	private String u_name;// 用户姓名
	private int u_gender;// 性别
	private String u_pass;// 用户密码
	private String u_idcard;// 身份证
	private String u_phone;// 用户电话
	private String u_picture;// 用户头像
	private String u_address;// 地址

	public int getU_id() {
		return u_id;
	}

	public void setU_id(int u_id) {
		this.u_id = u_id;
	}

	public String getU_code() {
		return u_code;
	}

	public void setU_code(String u_code) {
		this.u_code = u_code;
	}

	public String getU_name() {
		return u_name;
	}

	public void setU_name(String u_name) {
		this.u_name = u_name;
	}

	public int getU_gender() {
		return u_gender;
	}

	public void setU_gender(int u_gender) {
		this.u_gender = u_gender;
	}

	public String getU_pass() {
		return u_pass;
	}

	public void setU_pass(String u_pass) {
		this.u_pass = u_pass;
	}

	public String getU_idcard() {
		return u_idcard;
	}

	public void setU_idcard(String u_idcard) {
		this.u_idcard = u_idcard;
	}

	public String getU_phone() {
		return u_phone;
	}

	public void setU_phone(String u_phone) {
		this.u_phone = u_phone;
	}

	public String getU_picture() {
		return u_picture;
	}

	public void setU_picture(String u_picture) {
		this.u_picture = u_picture;
	}

	public String getU_address() {
		return u_address;
	}

	public void setU_address(String u_address) {
		this.u_address = u_address;
	}
}
